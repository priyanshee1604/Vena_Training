package com.priyanshee.FunctionalProgramming.Bonus.question55;

class Job {
    private String name;
    private String status;

    public Job(String name, String status) {
        this.name = name;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public String getStatus() {
        return status;
    }
}

